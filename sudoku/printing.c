#include "printing.h"

void	print_board(char **board)
{
	int r;
	int c;

	r = 0;
	c = 0;
	while (r < 9)
	{
		c = 0;
		while (c < 9)
		{
			write(1, &board[r][c], 1);
			if (c != 8)
				write(1, " ", 1);
			c++;
		}
		write(1, "\n", 1);
		r++;
	}
}
